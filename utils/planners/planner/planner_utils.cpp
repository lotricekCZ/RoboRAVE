#include <iostream>
#include "planner_utils.hpp"


std::vector<coordinates> plan_utils::closest_points(coordinates measured_point, std::vector<std::vector<coordinates>> source){
	std::vector<coordinates> ret;
	for(std::vector<coordinates> a: source) ret.push_back(measured_point.get_closest(a));
	return ret;
	}
